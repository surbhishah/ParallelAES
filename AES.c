//maps input array into the state matrix
void map_input_to_state(){
	
	unsigned int i=0,j=0;
	for(i=0;i<STATE_SIZE;i++){
		for(j=0;j<STATE_SIZE;j++){
			state[i][j] = in[i + 4*j];					// the elements of in[] are stored in column major order into the state matrix
			}
		}
	}
	
	

//compute GCD(a,b) efficiently
int GCD(int a,int b){
	if (b == 0){					//assume a > b	
		return a;
		}
	else{
		GCD(b,a%b);
		}
	}
	
//custom implementation of the modulo operator to return a positive modulus value 
int MOD(int a,int b){
		if (a == 0){
			return 0;
			}
		else if ((a>0 && b>0)||(a<0 && b<0)){
			return a%b;
			}
		else{
			return b+a%b;
			}
		}
		
		
		
unsigned char GF_multiplication(unsigned char a,unsigned char b){

	unsigned char prod = 0x0 , carry = 0x0 ;
	
	unsigned int i = 0;
	
	
	for (i = 0; i < 8; i++) {
		if (b & 1) 
			prod ^= a;
		carry = a & 0x80;  
		a <<= 1;
		if (carry) 
			a ^= 0x1B; 
		b >>= 1;
	}
	return prod;

}
	
