//used for substitution of values from the pre-computed sbox into the state matrix
void sub_bytes(){
	
	unsigned int i=0,j=0,l_idx = 0,r_idx = 0;
	
	for(;i<STATE_SIZE;i++){
		for(j=0;j<STATE_SIZE;j++){
			l_idx = (state[i][j]>>4) ;
			r_idx = (state[i][j] & 0x0F) ;
			state[i][j] = sbox[l_idx][r_idx];
			}
		}
	
	}
	
//shifts rows of the state to the left	
void shift_rows(){
	
	unsigned int i = 1,j = 0,k=0,l = 0;						// each element of row i is shifted left by i positions 
	unsigned char temp = 0x0;
	for(;i<STATE_SIZE;i++){
		int gcd = GCD(STATE_SIZE,i);					//optimal array shift algorithm 
		j=0;
		while(j<gcd){
			k=j;
			temp = state[i][j];
			while(1){
			l = (k + i)%STATE_SIZE;
			
			if (l == j){
				break;
				}
			state[i][k] = state[i][l];
			k = l;
			}
			state[i][k] = temp;
			j++;
				}
			}
		}
		
		
		

